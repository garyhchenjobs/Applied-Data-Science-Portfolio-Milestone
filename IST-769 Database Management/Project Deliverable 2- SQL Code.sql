-- DROP TABLES
DROP TABLE IF EXISTS working_set_pounds
DROP TABLE IF EXISTS workout_session
DROP TABLE IF EXISTS athlete_weight
DROP TABLE IF EXISTS pt_treatment
DROP TABLE IF EXISTS physical_therapy
DROP TABLE IF EXISTS daily_habits
DROP TABLE IF EXISTS athlete


-- CREATE TABLES(in reverse order of drop)
CREATE TABLE athlete (
	athlete_id varchar(10) PRIMARY KEY,
	first_name varchar(25) NOT NULL,
	last_name varchar(25) NOT NULL,
	email_address varchar(40) UNIQUE,
	gender varchar(1),
	height_cm int,
	-- Constraints
	CONSTRAINT CK_gender CHECK(gender IN ('M','F','T',NULL))
)

CREATE TABLE daily_habits(
	day_id int identity PRIMARY KEY,
	daily_date datetime NOT NULL,
	hours_of_sleep float NOT NULL,
	calories_consumed int NOT NULL,
	alcohol_intake int NOT NULL,
	athlete_id varchar(10) NOT NULL,
	-- Constraints
	CONSTRAINT FK1_athlete_id FOREIGN KEY (athlete_id) REFERENCES athlete(athlete_id)
) 

CREATE TABLE physical_therapy(
	pt_session_id varchar(10) PRIMARY KEY,
	week_id int NOT NULL,
	pt_datetime datetime,
	athlete_id varchar(10) NOT NULL,
	--Constraints
	CONSTRAINT FK2_athlete_id FOREIGN KEY (athlete_id) REFERENCES athlete(athlete_id)
)

CREATE TABLE pt_treatment(
	pt_treatment_id int identity PRIMARY KEY,
	treatment_area varchar(20) NOT NULL,
	pt_session_id varchar(10) NOT NULL,
	--Constraints
	CONSTRAINT FK1_pt_session_id FOREIGN KEY (pt_session_id) REFERENCES physical_therapy(pt_session_id)
)

CREATE TABLE athlete_weight(
	weight_id int identity PRIMARY KEY,
	week_id int NOT NULL,
	body_weight int NOT NULL,
	athlete_id varchar(10) NOT NULL,
	--CONSTRAINT
	CONSTRAINT FK3_athlete_id FOREIGN KEY (athlete_id) REFERENCES athlete(athlete_id)
)

CREATE TABLE workout_session(
	workout_id nvarchar(10) PRIMARY KEY,
	workout_date datetime NOT NULL,
	week_id int NOT NULL,
	start_time time NOT NULL,
	/* Had to change workout_duration_hrs and stretch_duration_mins
	to string type because dummy data gave formatting errors when trying to insert data into database. 
	Can't do calculations on these columns with that format, but at least we get an idea. 
	I was in too deep in and couldn't change the dummy data format. 
	*/
	workout_duration_hrs varchar(6) NOT NULL,
	stretch_duration_mins varchar(6) NOT NULL,
	workout_regime varchar(1) NOT NULL,
	athlete_id varchar(10) NOT NULL
	--CONSTRAINT
	CONSTRAINT FK4_athlete_id FOREIGN KEY (athlete_id) REFERENCES athlete(athlete_id),
	CONSTRAINT CHK1_workout_regime CHECK(workout_regime IN ('A','B'))
)

CREATE TABLE working_set_pounds(
	working_set_id int identity PRIMARY KEY,
	workout_regime varchar(1) NOT NULL,
	squat int NOT NULL,
	bench int,
	military_press int,
	deadlift int NOT NULL,
	workout_id nvarchar(10) NOT NULL,
	--CONSTRAINT
	CONSTRAINT FK1_workout_id FOREIGN KEY (workout_id) REFERENCES workout_session(workout_id)
)

