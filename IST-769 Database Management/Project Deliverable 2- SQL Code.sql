-- Average amount of sleep that each athlete gets 

SELECT
	a.first_name+ ' '+ a.last_name as athlete_name
	,AVG(db.hours_of_sleep) avg_hrs_sleep
FROM daily_habits db LEFT JOIN athlete a
ON db.athlete_id = a.athlete_id
GROUP BY a.first_name+ ' '+ a.last_name

--Maxium pounds that each athlete lifted for each exercise
SELECT
	a.first_name+ ' '+ a.last_name as athlete_name
	,MAX(wp.squat) max_squat
	,MAX(wp.bench) max_bench
	,MAX(wp.military_press) max_military_press
	,MAX(wp.deadlift) max_deadlift
FROM athlete a RIGHT JOIN workout_session ws 
ON a.athlete_id = ws.athlete_id LEFT JOIN working_set_pounds wp 
ON ws.workout_id =wp.workout_id
GROUP BY a.first_name+ ' '+ a.last_name ORDER BY MAX(wp.squat)

--Athlete that ate the most calories
SELECT
	a.first_name+ ' '+ a.last_name as athlete_name
	,SUM(db.calories_consumed) total_calories
FROM daily_habits db LEFT JOIN athlete a
ON db.athlete_id = a.athlete_id
GROUP BY a.first_name+ ' '+ a.last_name
ORDER BY SUM(db.calories_consumed) DESC

-- Athlete that had the most workout sessions
SELECT 
	a.first_name+ ' '+ a.last_name as athlete_name
	,COUNT(ws.workout_id) workouts 
FROM workout_session ws LEFT JOIN athlete a 
ON ws.athlete_id = a.athlete_id 
GROUP BY a.first_name+ ' '+ a.last_name
ORDER BY COUNT(ws.workout_id) DESC

